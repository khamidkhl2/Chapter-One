function renderGrid(id, list, withSoon){
  const grid=document.getElementById(id);
  if(!grid) return;
  let html=list.map((p,i)=>cardHTML(p,i)).join('');
  if(withSoon) html+=`
    <article class="card-soon reveal" data-soon>
      <div class="soon-ico">✨</div>
      <h3 data-i18n="soon.title"></h3>
      <p data-i18n="soon.text"></p>
    </article>`;
  grid.innerHTML=html;
}

