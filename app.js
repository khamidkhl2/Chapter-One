/* wire the colour filter (collection page only) */
function setupFilter(){
  const grid=document.getElementById('grid');
  const swHost=document.getElementById('swatchGrid');
  const allBtn=document.getElementById('filterAll');
  const empty=document.getElementById('emptyNote');
  if(!grid || !swHost) return;
  function apply(color){
    let shown=0;
    grid.querySelectorAll('article').forEach(a=>{
      const match = !color || a.dataset.color===color;
      a.classList.toggle('hide', !match);
      if(match && !a.hasAttribute('data-soon')) shown++;
    });
    swHost.querySelectorAll('.swatch[data-color]').forEach(s=>{
      s.classList.toggle('active', !!color && s.dataset.color===color);
      s.classList.toggle('dimmed', !!color && s.dataset.color!==color);
    });
    if(allBtn) allBtn.classList.toggle('active', !color);
    if(empty) empty.style.display = shown? 'none':'block';
  }
  swHost.querySelectorAll('.swatch[data-color]').forEach(s=>{
    s.addEventListener('click', ()=> apply(s.dataset.color));
    s.addEventListener('keydown', e=>{ if(e.key==='Enter'||e.key===' '){ e.preventDefault(); apply(s.dataset.color); } });
  });
  if(allBtn) allBtn.addEventListener('click', ()=> apply(null));
  apply(null);
}

