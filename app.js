(function init(){
  const saved = localStorage.getItem('chapterone_lang');
  const lang = (saved && translations[saved]) ? saved : 'ru';
  applyLang(lang);
  // splash only exists on the home page; hide it if the visitor already chose a language
  if(splash && saved) hideSplash();
})();

// splash language buttons
document.querySelectorAll('.lang-btn').forEach(btn=>{
  btn.addEventListener('click', ()=>{
    applyLang(btn.getAttribute('data-lang'));
    hideSplash();
  });
});

// navbar / drawer language switch buttons
document.querySelectorAll('[data-switch] button').forEach(btn=>{
  btn.addEventListener('click', ()=> applyLang(btn.getAttribute('data-set-lang')));
});

/* ===================== NAVBAR SCROLL ===================== */
const nav = document.getElementById('nav');
const onScroll = ()=> nav.classList.toggle('scrolled', window.scrollY > 40);
window.addEventListener('scroll', onScroll, {passive:true});
onScroll();

/* ===================== MOBILE DRAWER ===================== */
const burger = document.getElementById('burger');
const drawer = document.getElementById('drawer');
const scrim  = document.getElementById('scrim');
function toggleDrawer(open){
  drawer.classList.toggle('open', open);
  scrim.classList.toggle('open', open);
  burger.classList.toggle('open', open);
  document.body.style.overflow = open ? 'hidden' : '';
}
burger.addEventListener('click', ()=> toggleDrawer(!drawer.classList.contains('open')));
scrim.addEventListener('click', ()=> toggleDrawer(false));
document.querySelectorAll('[data-close]').forEach(a=> a.addEventListener('click', ()=> toggleDrawer(false)));

/* ===================== REVEAL ON SCROLL ===================== */
const io = new IntersectionObserver((entries)=>{
  entries.forEach(e=>{ if(e.isIntersecting){ e.target.classList.add('is-visible'); io.unobserve(e.target);} });
},{threshold:0.14, rootMargin:'0px 0px -8% 0px'});
document.querySelectorAll('.reveal').forEach(el=> io.observe(el));

/* ===================== SCATTERED GOLD STARS ===================== */
function scatterStars(){
  const stars = ['✦','✧','★','✶','✦'];
  const layers = [
    {sel:'.hero', count:16, min:9, max:18},
    {sel:'.collection', count:10, min:8, max:14},
    {sel:'.mission', count:24, min:8, max:16, color:'#E8D08A'},
    {sel:'.about', count:8, min:8, max:14}
  ];
  layers.forEach(L=>{
    const host = document.querySelector(L.sel);
    if(!host) return;
    if(getComputedStyle(host).position==='static') host.style.position='relative';
    for(let i=0;i<L.count;i++){
      const s=document.createElement('span');
      s.className='star';
      s.textContent=stars[Math.floor(Math.random()*stars.length)];
      s.style.left=(Math.random()*96+2)+'%';
      s.style.top=(Math.random()*90+3)+'%';
      s.style.fontSize=(Math.random()*(L.max-L.min)+L.min)+'px';
      s.style.animationDelay=(Math.random()*4)+'s';
      s.style.animationDuration=(3+Math.random()*3)+'s';
      if(L.color) s.style.color=L.color;
      host.appendChild(s);
    }
  });
}
scatterStars();
